// Create a variable called score and give it a value between 0 and 100
let score =100;

// Create a variable called passingGrade and set it to 60
const passingGrade = 60;

// Check if the score is perfect
if (score === 100) {
    console.log("Perfect score!");
}
// Check if the score is greater than or equal to passingGrade
else if (score >= passingGrade) {
    console.log("Passed");
}
// If the score is below passingGrade
else {
    console.log("Failed");
}
